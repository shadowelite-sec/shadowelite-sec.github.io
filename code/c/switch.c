#include <stdio.h>
int main()
{
	int choice = 3;
	switch(choice)
	{
	case 1:
		printf("\nI am in 1");
		break;

	case 2:
		printf("\nI am in 2");
		break;

	case 3:
		printf("\nI am in 3");
		break;

	case 4:
		printf("\nI am in 4");
		break;

	default:
		printf("\n I am Default");
		break;
	}
	return 0;
}

